# Программа для решения одномерного уравнения теплопроводности
# Использован ЯВНЫЙ конечно-разностный метод

# --- 1. Параметры задачи ---
L = 0.02           # Толщина стенки (м)
lambda_ = 15.0     # Коэффициент теплопроводности (Вт/м·К)
c = 500.0          # Удельная теплоемкость (Дж/кг·К)
rho = 7900.0       # Плотность материала (кг/м³)
T0 = 300.0         # Начальная температура стенки (°C)

# Граничные условия 3-го рода (конвективный теплообмен)
# alpha1, alpha2 - коэффициенты теплоотдачи (Вт/м²·К)
# Tg1, Tg2 - температуры окружающей среды (°C)
alpha1, Tg1 = 2000.0, 20.0
alpha2, Tg2 = 500.0, 80.0

# --- 2. Параметры сетки ---
Nx = 20                # Количество расчетных узлов
dx = L / (Nx - 1)      # Шаг по пространству (м)
a = lambda_ / (c * rho)# Коэффициент температуропроводности

# Критерий Фурье (Fo). Условие устойчивости: Fo <= 0.5
# Если Fo > 0.5, расчет пойдет "вразнос" (температура станет бесконечной)
Fo = 0.4               
dt = Fo * (dx**2) / a  # Автоматический расчет шага по времени (с)

# --- 3. Инициализация температуры ---
# Создаем список из Nx элементов, заполненный начальной температурой
T = [T0] * Nx

# --- 4. Основной цикл расчета ---
# Вычисляем температуру на каждом новом шаге по времени
for step in range(500): 
    T_new = [0.0] * Nx # Создаем временный массив для следующего шага

    # 1. ЛЕВАЯ ГРАНИЦА (Условие 3-го рода, балансовый метод)
    Bi1 = (alpha1 * dx) / lambda_
    # Формула учитывает теплообмен конвекцией (Tg1-T[0]) 
    # и теплопроводность внутри стенки (T[1]-T[0])
    T_new[0] = T[0] + 2 * Fo * (T[1] - T[0] + Bi1 * (Tg1 - T[0]))

    # 2. ВНУТРЕННИЕ УЗЛЫ (Основное уравнение теплопроводности)
    # Значение температуры зависит от температуры соседа слева и справа
    for i in range(1, Nx - 1):
        T_new[i] = T[i] + Fo * (T[i+1] - 2*T[i] + T[i-1])

    # 3. ПРАВАЯ ГРАНИЦА (Условие 3-го рода, балансовый метод)
    Bi2 = (alpha2 * dx) / lambda_
    T_new[Nx-1] = T[Nx-1] + 2 * Fo * (T[Nx-2] - T[Nx-1] + Bi2 * (Tg2 - T[Nx-1]))

    # Переход к новому временному слою
    T = T_new

# --- 5. Вывод результатов ---
print("Распределение температуры по узлам:")
print([round(t, 2) for t in T])
