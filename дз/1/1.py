import numpy as np
import matplotlib.pyplot as plt
import os

# =====================================================================
# 1. ИСХОДНЫЕ ДАННЫЕ
# =====================================================================
# Геометрия и теплофизические свойства 
L = 0.1          # Толщина стенки, м
lambda_coeff = 50.0 # Коэффициент теплопроводности, Вт/(м*К)
rho = 7800.0     # Плотность, кг/м^3
cp = 460.0       # Удельная теплоемкость, Дж/(кг*К)
a = lambda_coeff / (rho * cp) # Коэффициент температуропроводности, м^2/с

# Начальные условия
T0 = 20.0        # Начальная температура стенки, °C

# Граничные условия (Вариант 11) - ГУ III рода с обеих сторон
# Левая граница (x = 0)
Tg1 = 100.0      # Температура греющей среды, °C
alpha1 = 500.0   # Коэффициент теплоотдачи, Вт/(м^2*К)

# Правая граница (x = L)
Tg2 = 30.0       # Температура охлаждающей среды, °C
alpha2 = 200.0   # Коэффициент теплоотдачи, Вт/(м^2*К)

# =====================================================================
# 2. ПАРАМЕТРЫ СЕТКИ И УСЛОВИЕ УСТОЙЧИВОСТИ
# =====================================================================
Nx = 50                  # Количество пространственных узлов
dx = L / (Nx - 1)        # Шаг по пространству, м
x = np.linspace(0, L, Nx) # Массив координат

# Сеточные числа для левой и правой границ
Bi1 = alpha1 * dx / lambda_coeff
Bi2 = alpha2 * dx / lambda_coeff

# Критерий Куранта (устойчивость явной схемы).
# Для внутренних узлов требуется Fo <= 0.5.
# Для граничных узлов с ГУ III рода условие : Fo <= 1 / (2 * (1 + Bi))
max_Bi = max(Bi1, Bi2)
Fo_max = 1.0 / (2.0 * (1.0 + max_Bi))

# Задаем рабочий шаг по времени с запасом 10% от предельного
Fo = 0.9 * Fo_max
dt = Fo * (dx**2) / a    # Шаг по времени, с

total_time = 1800.0      # Общее время моделирования, с
steps = int(total_time / dt)

# =====================================================================
# 3. И_-__-___РАСЧЕТ
# =====================================================================
T = np.full(Nx, T0)      # Температурное поле на текущем слое
T_new = np.copy(T)       # Температурное поле на следующем слое

# Моменты времени для сохранения профилей (в секундах)
save_times = [60, 300, 600, 1200, 1800]
results = {0: np.copy(T)}

print(f"Запуск расчета: шаг dx = {dx:.5f} м, шаг dt = {dt:.3f} с")
print(f"Сеточное число Фурье (Fo) = {Fo:.4f}")

for s in range(1, steps + 1):
    current_time = s * dt
    
    # 3.1 Расчет внутренних узлов (Уравнение теплопроводности)
    for i in range(1, Nx - 1):
        T_new[i] = T[i] + Fo * (T[i+1] - 2*T[i] + T[i-1])
    
    # 3.2 Расчет граничных узлов (Аппроксимация ГУ III рода)
    # Метод теплового баланса 
    T_new[0] = T[0] + 2 * Fo * (T[1] - T[0] + Bi1 * (Tg1 - T[0]))
    T_new[-1] = T[-1] + 2 * Fo * (T[-2] - T[-1] + Bi2 * (Tg2 - T[-1]))
    
    # Обновление поля температур
    T[:] = T_new[:]
    
    # Сохранение результатов в заданные моменты времени
    for t_save in save_times:
        if abs(current_time - t_save) < dt / 2:
            results[t_save] = np.copy(T)

# =====================================================================
# 4. ВЫВОД РЕЗУЛЬТАТОВ (Графики и Файл)
# =====================================================================
# Запись в файл
filename = "results_variant_11.txt"
with open(filename, "w", encoding="utf-8") as f:
    f.write("Распределение температур для последнего шага (t = 1800 с)\n")
    f.write("x [m]\tT [C]\n")
    for i in range(Nx):
        f.write(f"{x[i]:.4f}\t{T[i]:.2f}\n")
print(f"Результаты успешно записаны в файл: {os.path.abspath(filename)}")

# Построение графиков
plt.figure(figsize=(10, 6))
for t_val, temp_array in sorted(results.items()):
    plt.plot(x * 100, temp_array, label=f'τ = {t_val} с', linewidth=2)

plt.title("Динамика прогрева бесконечной стенки (Вариант 11)\nЯвный метод конечных разностей", fontsize=14)
plt.xlabel("Координата x, см", fontsize=12)
plt.ylabel("Температура T, °C", fontsize=12)
plt.legend(fontsize=11)
plt.grid(True, linestyle='--', alpha=0.7)
plt.tight_layout()
plt.show()