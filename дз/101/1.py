import numpy as np
import matplotlib.pyplot as plt

# =================================================================
# 1. ГЕОМЕТРИЯ И СЕТКА 
# =================================================================
L_x = 0.1   # Длина бруса (ось X)
L_y = 0.05  # Высота бруса (ось Y)
nx = 50     # Сколько точек будет по горизонтали
ny = 50     # чИсЛо точЕк по вертиКали

# Находим расстояние между точками (ну шаг сетки)
dx = L_x / (nx - 1) 
dy = L_y / (ny - 1)

# =================================================================
# 2.  (Св-ва стали и время)
# =================================================================
lam = 45.0          # Теплопроводность 
rho = 7800.0        # Плотность кг на куб
cp = 460.0          # Теплоемкость
# Альфа коэффициент температуропроводности 
a_diff = lam / (rho * cp)  

# Расчет шага по времени (dt). 
#ачё
dt = 0.25 * min(dx**2, dy**2) / a_diff
total_time = 100.1  # Считаем до 100 секунд
steps = int(total_time / dt) #  количество итераций всего

# =================================================================
# 3. ГРАНИЧНЫЕ УСЛОВИЯ (Варик 11)
# =================================================================
T_env_left = 600.0   # Температура среды слева
alpha_left = 1500.0  # Коэффициент теплоотдачи слева (III род)

T_env_right = 300.0  # Температура среды справа
alpha_right = 200.0  # Коэффициент теплоотдачи справа (III род

q_bottom = 200000.0  # увеличил приток тепла снизу (II род,
q_top = 5000.0       # Небольшой уход тепла сверху (II род

T0 = 293.15          # Начальная температура бруса 20 дегриз

# =================================================================
# 4. ПОДГОТОВКА МАССИВОВ
# =================================================================
# Создаем основную таблицу температур T и заполняем её начальным значением T0 
T = np.full((nx, ny), T0)

# Список моментов времени для  результатов (1с, 50с и 100с)
save_times = [1.0, 50.0, 100.0]
saved_count = 0

# =================================================================
# 5. ГЛАВНЫЙ ЦИКЛ (Считаем шаг за шагом)
# =================================================================
for n in range(steps):
    current_time = n * dt # Текущее время в секундах
    Tn = T.copy()         # Сохраняем предыдущий шаг для расчетов
    
    # 5.1. СОЗДАНИЕ "РАМКИ" 
    # Создам матрицу на 2 элемента больше по каждой оси, чтобы учесть граничные условия
    T_ext = np.zeros((nx + 2, ny + 2))
    T_ext[1:-1, 1:-1] = Tn # кинул брус внутрь этой "рамки"
    
    # 5.2. ПРИМЕНЯЕМ ГРАНИЧНЫЕ УСЛОВИЯ (Заполняем рамку значениями)
    # Лево (III род): учитываем теплоотдачу от среды
    T_ext[0, 1:-1] = Tn[1, :] + (2 * dx * alpha_left / lam) * (T_env_left - Tn[0, :])
    
    # Право (III род): учитываем теплоотдачу в среду
    T_ext[-1, 1:-1] = Tn[-2, :] - (2 * dx * alpha_right / lam) * (Tn[-1, :] - T_env_right)
    
    # Низ (II род): задаем фиксированный тепловой поток q
    T_ext[1:-1, 0] = Tn[:, 1] + (2 * dy * q_bottom / lam)
    
    # Верх (II род): задаем уходящий поток q
    T_ext[1:-1, -1] = Tn[:, -2] - (2 * dy * q_top / lam)
    
    # 5.3. МАТЕМАТИКА (Вторые производные — Лапласиан)
    # Считаем, как меняется температура по X и по Y
    d2T_dx2 = (T_ext[2:, 1:-1] - 2 * Tn + T_ext[:-2, 1:-1]) / dx**2
    d2T_dy2 = (T_ext[1:-1, 2:] - 2 * Tn + T_ext[1:-1, :-2]) / dy**2
    
    # 5.4. ОБНОВЛЕНИЕ ТЕМПЕРАТУРЫ (Само уравнение теплопроводности)
    T = Tn + a_diff * dt * (d2T_dx2 + d2T_dy2)

    # =================================================================
    # 6. ВЫВОД РЕЗУЛЬТАТОВ (По графику на 1с, 50с и 100с)
    # =================================================================
    if saved_count < len(save_times) and current_time >= save_times[saved_count]:
        # Рисую  карту
        plt.figure(figsize=(7, 4))
        # T.T — делаюююю матрицу, чтобы X был по горизонтали, а Y по вертикали
        plt.imshow(T.T, extent=[0, L_x, 0, L_y], origin='lower', cmap='inferno', aspect='auto')
        plt.colorbar(label='Температура, K')
        plt.title(f'Время: {current_time:.2f} сек')
        plt.xlabel('Длина (м)')
        plt.ylabel('Высота (м)')
        plt.show() #ждб
        
        # Сохраняем цифры в файл .csv 
        fname = f'temp_at_{int(current_time)}s.csv'
        np.savetxt(fname, T, delimiter=',')
        print(f'>>> Данные на {current_time:.1f}с записаны в файл {fname}')
        
        saved_count += 1

print("Готово; Все файлы сохранены в папку с кодом.")